void main() {
// 정수
// integer
    int number1 = 10;
    print(number1);
    
    int number2 = 15;
    print(number2);
    
    int number3 = -20;
    print(number3);
    
  // 실수
  // double
    double n1 = 2.5;
    double n2 = 0.5;
    double n3 = 4.0;
    
    print(n1 + n2);
    print(n1 - n2);
    print(n1 / n2);
    print(n1 * n2);

  // 계산
    int num1 = 2;
    int num2 = 4;
    print(num1 + num2);
    print(num1 - num2);
    print(num1 / num2);
    print(num1 * num2);

    print(num1 % 2);
    print(num1 % 3);
    // 나머지
    num1 ++; // 1 증가
    num1 --; // 1 감소

    n3 += 1; // 1 더하기
    n3 -= 1; // 1 빼기
    n3 *= 2; // 2 곱하기
    n3 /= 2; // 2 나누기
}